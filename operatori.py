# 1. Operatori Aritmetici

# - Operatorii aritmetici în Python sunt utilizați pentru efectuarea operațiilor matematice de bază. Aceștia permit manipularea valorilor numerice.

# | Nume               | Operator   |
# | ------------------ | ---------- |
# | Adunare            | **`+`**    |
# | Scadere            | **`-`**    |
# | Inmultire          | **`*`**    |
# | Impartire          | **`/`**    |
# | Modul              | **`%`**    |
# | Impartire intreaga | **`//`**   |
# | Exponentiere       | **`**`\*\* |

Var_1 = 1
Var_2 = 2
Var_3 = 10
Var_4 = 15
Var_5 = Var_1 + Var_3
Var_5 -= 1
Var_6 = Var_4 - Var_3
Var_7 = Var_6 * Var_2
Var_8 = Var_7 ** Var_2
Var_9 = Var_3 / Var_2
Var_10 = Var_3 % Var_2
Var_11 = Var_9 // Var_2
print(Var_5, Var_6, Var_7, Var_8, Var_9, Var_10, Var_11)

# 2. Operatori de comparatie
# Operatorii de comparație în Python sunt utilizați pentru a evalua relațiile de ordine între valori. Aceștia returnează rezultate de tip boolean (True sau False) în funcție de condiția specificată.

print(Var_1 == Var_2)
print(Var_1 != Var_2)
print(Var_1 < Var_2)
print(Var_1 > Var_2)
print(Var_1 <= Var_2)
print(Var_1 >= Var_2)

